package com.glsite.myapp;

public class Calc {
    /**
     * 两个整数想加
     * @param a
     *          整数1
     * @param b
     *          整数2
     * @return  两个整数的和
     */
    public static int add(int a, int b) {
        return a + b;
    }

    /**
     * 两个整数的差
     * @param a
     *          整数1
     * @param b
     *  `       整数2
     * @return  两个整数的差
     */
    public static int sub(int a, int b) {
        return a - b;
    }

    /**
     * 两个整数的积
     * @param a
     *          整数1
     * @param b
     *  `       整数2
     * @return  两个整数的积
     */
    public static int mul(int a, int b) {
        return a * b;
    }
}
